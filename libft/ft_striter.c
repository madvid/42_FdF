#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t		i;
	size_t		ls;

	i = 0;
	if (s != NULL && f != NULL)
	{
		ls = ft_strlen(s);
		while (i < ls)
		{
			(*f)(&s[i]);
			i++;
		}
	}
}
